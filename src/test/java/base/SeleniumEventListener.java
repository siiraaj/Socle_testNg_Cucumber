	public boolean testJquery(WebDriver driver) {
		JavascriptExecutor js = (JavascriptExecutor) driver;
		return (Boolean) js.executeScript("return !!window.jQuery");
	}

	public void waitForAjaxComplete(WebDriver driver) {
		int numberLoop = 100;
		int numberAjaxCompleteTrueExpected = 3;
		int numberAjaxCompleteTrueAcuel = 0;
		JavascriptExecutor js = (JavascriptExecutor) driver;
		for (int i = 1; i < numberLoop; i++) {
			try {
				Thread.sleep(20);
			} catch (InterruptedException e) {
				e.printStackTrace();
			}
			boolean code = (Boolean) js.executeScript("return !!window.jQuery && jQuery.active == 0");
			if (code) {
				numberAjaxCompleteTrueAcuel++;
			} else {
				numberAjaxCompleteTrueAcuel = 0;
			}
			if (numberAjaxCompleteTrueAcuel > numberAjaxCompleteTrueExpected) {
				break;
			}
		}
	}	
	

